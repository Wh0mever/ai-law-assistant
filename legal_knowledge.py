"""
База юридических знаний с актуальной информацией
"""

class LegalKnowledge:
    """Актуальная юридическая информация для ИИ"""
    
    BANKRUPTCY_PRACTICE_2024 = """
    АКТУАЛЬНАЯ ПРАКТИКА ПО БАНКРОТСТВУ (II квартал 2024 года):
    
    1. ИСПОЛНИТЕЛЬСКИЙ ИММУНИТЕТ:
    - На деньги от продажи единственного ипотечного жилья банкрота распространяется исполнительский иммунитет
    - Единственное жилье умершего должника может сохранить исполнительский иммунитет  
    - Нелегализованное жилье может обладать исполнительским иммунитетом
    - Размер "защищенных" средств могут уменьшить при недобросовестности должника
    
    2. ОЧЕРЕДНОСТЬ РАСЧЕТОВ:
    - Долги по текущим налогам, связанным с предметом залога, погашаются по итогам банкротных торгов до расчетов с залоговым кредитором
    - Очередность требования ФНС по восстановленному НДС зависит от периода операции
    
    3. СУБСИДИАРНАЯ ОТВЕТСТВЕННОСТЬ:
    - Субсидиарную ответственность брошенной организации продолжают нести ее собственники
    - Стоимость имущества по недействительной сделке могут взыскать не только с покупателя, но и с того, кто помогал выводить активы
    
    4. ФИНАНСИРОВАНИЕ ПРОЦЕДУР:
    - Если кредитор явно не укажет лимит финансирования, он обязан полностью оплатить услуги управляющего
    - Управляющий не обязан сделать нечто экстраординарное для получения стимулирующего вознаграждения
    """
    
    BANKRUPTCY_PROCEDURE = """
    ПРОЦЕДУРА БАНКРОТСТВА ГРАЖДАН:
    
    1. СУДЕБНЫЙ ПОРЯДОК:
    - Требования от 500 тысяч рублей
    - Неисполнение в течение 3 месяцев
    - Обязательное обращение при невозможности исполнения обязательств перед другими кредиторами
    
    2. ВНЕСУДЕБНОЕ БАНКРОТСТВО:
    - От 25 тысяч до 1 миллиона рублей
    - Особые основания: окончено исполнительное производство, получатель пенсии или пособия на ребенка
    - Подача заявления в МФЦ
    - Процедура длится 6 месяцев
    
    3. ПРИЗНАКИ НЕПЛАТЕЖЕСПОСОБНОСТИ:
    - Прекращение расчетов с кредиторами
    - Неисполнение более 10% обязательств в течение месяца
    - Размер задолженности превышает стоимость имущества
    - Постановление об окончании исполнительного производства
    
    4. ПОСЛЕДСТВИЯ БАНКРОТСТВА:
    - 5 лет ограничения на кредиты
    - Ограничения на управление организациями (3-10 лет)
    - Освобождение от требований кредиторов (кроме исключительных случаев)
    """
    
    LEGAL_REFERENCES = """
    ОСНОВНЫЕ НОРМАТИВНЫЕ АКТЫ:
    
    1. Федеральный закон от 26.10.2002 N 127-ФЗ "О несостоятельности (банкротстве)"
    2. Арбитражный процессуальный кодекс РФ
    3. Гражданский процессуальный кодекс РФ
    4. Постановления Пленума ВС РФ от 13.10.2015 N 45
    5. Постановления КС РФ от 04.06.2024 N 28-П
    6. Постановления КС РФ от 09.04.2024 N 16-П
    
    ПРОЦЕДУРНЫЕ ОСОБЕННОСТИ:
    - Рассмотрение дел в арбитражном суде по месту жительства должника
    - Внесение денежных средств в депозит суда на оплату управляющего
    - Публикация сведений в ЕФРСБ
    - Возможность мирового соглашения на любой стадии
    """
    
    COMPLAINT_STRUCTURE = """
    СТРУКТУРА АПЕЛЛЯЦИОННОЙ/КАССАЦИОННОЙ ЖАЛОБЫ:
    
    1. ВВОДНАЯ ЧАСТЬ:
    - Наименование суда, в который подается жалоба
    - ФИО, адрес, контактные данные заявителя
    - ФИО представителя (при наличии)
    - Наименование других лиц, участвующих в деле
    - Реквизиты обжалуемого судебного акта
    
    2. ОПИСАТЕЛЬНАЯ ЧАСТЬ:
    - Краткое изложение обстоятельств дела
    - Требования заявителя
    - Решение суда первой инстанции
    - Доводы суда
    
    3. МОТИВИРОВОЧНАЯ ЧАСТЬ:
    - Нарушения норм материального права
    - Нарушения норм процессуального права
    - Неправильное установление обстоятельств дела
    - Ссылки на законодательство и судебную практику
    
    4. ПРОСИТЕЛЬНАЯ ЧАСТЬ:
    - Просьба об отмене решения
    - Просьба о принятии нового решения
    - Альтернативные требования
    
    5. ПРИЛОЖЕНИЯ:
    - Копия обжалуемого решения
    - Документы, подтверждающие доводы жалобы
    - Доверенность (при подаче через представителя)
    """
    
    @classmethod
    def get_system_prompt_for_practice(cls):
        """Системный промпт для поиска судебной практики"""
        return f"""
        Вы - опытный юрист, специализирующийся на российском праве и судебной практике.
        
        Используйте актуальную информацию:
        {cls.BANKRUPTCY_PRACTICE_2024}
        
        {cls.BANKRUPTCY_PROCEDURE}
        
        {cls.LEGAL_REFERENCES}
        
        При анализе ситуации:
        1. Определите правовую область
        2. Выделите ключевые правовые вопросы  
        3. Найдите релевантную судебную практику
        4. Укажите применимые нормы права
        5. Дайте рекомендации по действиям
        
        Ответ структурируйте по разделам с конкретными ссылками на законы и практику.
        """
    
    @classmethod
    def get_system_prompt_for_complaint(cls):
        """Системный промпт для составления жалоб"""
        return f"""
        Вы - опытный юрист по составлению апелляционных и кассационных жалоб.
        
        Структура жалобы:
        {cls.COMPLAINT_STRUCTURE}
        
        Актуальная практика:
        {cls.BANKRUPTCY_PRACTICE_2024}
        
        Требования к жалобе:
        - Четкая структура согласно АПК РФ
        - Конкретные ссылки на нарушенные нормы
        - Обоснование каждого довода
        - Соответствие процессуальным требованиям
        
        Составьте профессиональную жалобу с учетом актуальной судебной практики.
        """
    
    @classmethod  
    def get_system_prompt_for_check(cls):
        """Системный промпт для проверки документов"""
        return f"""
        Вы - опытный юрист-эксперт по проверке юридических документов.
        
        Актуальное законодательство:
        {cls.LEGAL_REFERENCES}
        
        Проверьте документ на:
        1. ФОРМАЛЬНЫЕ ОШИБКИ:
           - Отсутствующие реквизиты
           - Неправильное оформление
           - Опечатки и грамматические ошибки
        
        2. ПРАВОВЫЕ ОШИБКИ:
           - Неправильные ссылки на законы
           - Неактуальные нормы
           - Противоречия в тексте
        
        3. ЮРИДИЧЕСКИЕ РИСКИ:
           - Основания для отклонения
           - Слабые места в аргументации
           - Процедурные нарушения
        
        4. РЕКОМЕНДАЦИИ:
           - Конкретные предложения по улучшению
           - Дополнительные документы
           - Усиление правовой позиции
        
        Дайте структурированный анализ с практическими рекомендациями.
        """ 